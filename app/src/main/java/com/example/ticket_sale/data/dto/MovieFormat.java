package com.example.ticket_sale.data.dto;

public class MovieFormat {
    private String id;
    private String name;

    public MovieFormat() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
